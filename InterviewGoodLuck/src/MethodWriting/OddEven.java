package MethodWriting;

public class OddEven {

	
	public static void Odd(int num) {
		
	     if ( num % 2 == 0 )
		        System.out.println("Entered number is even");
		     else
		        System.out.println("Entered number is odd");
	}
	
	public static void main(String[] args) {
		
		Odd(21);
		
	}

}

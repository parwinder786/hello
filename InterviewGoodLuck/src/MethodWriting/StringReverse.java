package MethodWriting;

public class StringReverse {

	            static void reverseString(String x) {
	            	
	            	StringBuffer ab = new StringBuffer(x);
	            	System.out.println(ab.reverse());
	            	
	            }
	            	
	public static void main(String[] args) {
		              
		           StringReverse.reverseString("jaspreet");

	}

}
